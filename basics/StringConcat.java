package basics;

public class StringConcat {
public static void main(String[] args) {
	//Creating three String constant objects
	String str="";
	String str1="Mridul";
	String str2="Mahajan";
	
	//concatenating str1 and str2 and storing the result in str
	str=str1+" "+ str2;
	//displaying result on the screen
	System.out.println(str);
}
}
